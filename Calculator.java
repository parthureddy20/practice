
public class Calculator {
	int arithmeticSum(int num1, int num2) {
		return num1+num2;
	}
	int arithmeticSubtraction(int num1, int num2) {
		return num1-num2;
	}
	int multiplication(int num1, int num2) {
		return num1*num2;
	}
	double division(int num1, int num2) {
		return num1/num2;
	}
	int modulodivision(int num1, int num2) {
		return num1%num2;
	}
	
}
